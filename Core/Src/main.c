void copyCleanedString(char* destination, const char* input) {
    size_t cleaned_index = 0;
    for (size_t i = 0; input[i] != '\0'; i++) {
        char c = input[i];

        // Check if the character is a visible ASCII character
        if (c >= 32 && c <= 126) {
            destination[cleaned_index++] = c;
        }
    }

    destination[cleaned_index] = '\0';
}





